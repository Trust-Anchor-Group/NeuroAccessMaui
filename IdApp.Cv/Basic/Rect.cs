namespace IdApp.Cv.Basic
{
	/// <summary>
	/// Represents a rectangle in an image.
	/// </summary>
	public struct Rect
	{
		/// <summary>
		/// Top X-Coordinate
		/// </summary>
		public int Left;

		/// <summary>
		/// Top Y-Coordinate
		/// </summary>
		public int Top;

		/// <summary>
		/// Width of rectangle.
		/// </summary>
		public int Width;

		/// <summary>
		/// Height of rectangle.
		/// </summary>
		public int Height;
	}
}
