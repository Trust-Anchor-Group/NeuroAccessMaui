namespace NeuroAccessMaui.UI.Controls
{
	/// <summary>
	/// Defines the placement of tab items within a <see cref="TabNavigationHost"/>.
	/// </summary>
	public enum TabPlacement
	{
		/// <summary>
		/// Tabs are displayed at the top with a horizontal layout.
		/// </summary>
		Top,

		/// <summary>
		/// Tabs are displayed at the bottom with a horizontal layout.
		/// </summary>
		Bottom,

		/// <summary>
		/// Tabs are displayed on the left with a vertical layout.
		/// </summary>
		Left,

		/// <summary>
		/// Tabs are displayed on the right with a vertical layout.
		/// </summary>
		Right
	}
}
