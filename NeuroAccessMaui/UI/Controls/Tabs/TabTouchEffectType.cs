namespace NeuroAccessMaui.UI.Controls
{
	/// <summary>
	/// Defines the touch feedback effect applied when interacting with tab items.
	/// </summary>
	public enum TabTouchEffectType
	{
		/// <summary>
		/// No additional feedback is applied.
		/// </summary>
		None,

		/// <summary>
		/// Tab items briefly adjust their opacity when touched.
		/// </summary>
		OpacityHighlight,

		/// <summary>
		/// Tab items briefly scale when touched.
		/// </summary>
		Scale
	}
}
